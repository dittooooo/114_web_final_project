import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      trim: true,
      minlength: 2,
      maxlength: 30,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
      lowercase: true,
    },
    password: { type: String, required: true }, // hashed
  },
  { timestamps: { createdAt: "createdAt", updatedAt: false } }
);

export default mongoose.model("User", userSchema);
